export function buildCorpus({ answers = [], materials = [] }) {
  const parts = ["# Ответы на интервью\n"];
  for (const a of answers) {
    parts.push(`## ${a.q}`);
    parts.push(a.transcript || "(нет ответа)");
    parts.push("");
  }
  if (materials.length) {
    parts.push("# Дополнительные материалы\n");
    for (const m of materials) {
      parts.push(`## [${m.type}]`);
      parts.push(m.text || "");
      parts.push("");
    }
  }
  return parts.join("\n");
}

